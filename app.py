    try:
        response = requests.get(f"{url}{requests.utils.quote(prompt)}", timeout=30)
        if response.status_code == 200 and response.text:
            return response.text
    except Exception:
        pass
        
    # Резервный шлюз через POST в случае тайм-аута основного канала
    payload = {
        "messages": [{"role": "user", "content": prompt}],
        "private": True
    }
    res = requests.post(url, json=payload, timeout=30)
    return res.text
    
