# РАЗДЕЛ 3. ФУНКЦИЯ ВЗАИМОДЕЙСТВИЯ С ИИ (ПРОМПТ)
# =====================================================================
def generate_perfect_math(subject, section, objectives_list, w_type, var_qty, t_count, score):
    client = Client()
    
    # Формируем список целей в строку
    cos_string = "\n".join([f"- {co}" for co in objectives_list])
    
    prompt = f"""
    Ты — опытный учитель математики, составитель методических материалов для школ Казахстана (ЕМН). 
    Твоя задача — составить контрольно-измерительный материал уровня 'Педагог-исследователь'.
    
    Предмет: {subject}
    Раздел/Четверть: {section}
    Тип работы: {w_type}
    Количество вариантов: {var_qty}
    Количество заданий в одном варианте: {t_count}
    Максимальный балл за работу: {score}
    
    Цели обучения (ГОСО РК), которые должны быть строго проверены в работе:
    {cos_string}
    
    СТРУКТУРА ВЫВОДА (Ты ДОЛЖЕН обернуть КАЖДЫЙ вариант в HTML-тег <div class="vzaimo-card">):
    
    <div class="vzaimo-card">
        <h2>{w_type} — {subject} (ЕМН)</h2>
        <div class="spec-block">
            <b>Раздел:</b> {section}<br>
            <b>Проверяемые цели:</b> {cos_string}<br>
            <b>Максимальный балл:</b> {score}
        </div>
        <h3>ВАРИАНТ №...</h3>
        <ol class="tasks-list">
            <li>[Условие задачи №1 с четкими математическими данными]</li>
            <li>[Условие задачи №2]...</li>
        </ol>
        
        <h3>Спецификация и критерии оценивания к Варианту №...</h3>
        <table>
            <thead>
                <tr>
                    <th>№ задания</th>
                    <th>Проверяемая ЦО</th>
                    <th>Дескриптор (Пошаговый ход решения)</th>
                    <th>Балл</th>
                </tr>
            </thead>
            <tbody>
                <tr>
                    <td>1</td>
                    <td>Из выбранных ЦО</td>
                    <td>- Правильно применяет формулу...<br>- Выполняет вычисления...</td>
                    <td>...</td>
                </tr>
            </tbody>
        </table>
        
        <h3>Ответы / Решения:</h3>
        <p>Подробные ключи ответов для учителя.</p>
    </div>
    
    ВАЖНО: Никакого лишнего текста до и после HTML-кода. Форматируй математические формулы аккуратно, используя общепринятые обозначения. Для геометрии пиши текстовые описания стереометрических фигур максимально понятно для построения чертежа.
    """
    
    response = client.chat.completions.create(
        model="gpt-4o",
        messages=[{"role": "user", "content": prompt}]
    )
    return response.choices[0].message.content

