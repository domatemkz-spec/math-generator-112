        if not class_templates:
            for subj, classes in templates.items():
                for cls, tasks_list in classes.items():
                    if cls in selected_class and subj == subject:
                        class_templates = tasks_list
                        break
                if class_templates:
                    break
        
        # Если все еще не нашли, берем из математики
        if not class_templates and "Математика" in templates:
            for cls in templates["Математика"]:
                if cls in selected_class:
                    class_templates = templates["Математика"][cls]
                    break
        
        # Если совсем пусто, берем из 5 класса
        if not class_templates:
            class_templates = templates["Математика"]["5 класс"]
        
