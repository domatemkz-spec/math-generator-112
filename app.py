def clean_generated_text(raw_text):
    """
    Очистка и подготовка сгенерированного текста
    """
    try:
        # Удаляем маркеры кода
        clean_text = str(raw_text).replace("```html", "").replace("```", "")
        clean_text = clean_text.replace("```python", "").replace("```", "")
        
        # Конвертируем LaTeX
        clean_text = clean_text.replace(r"\[", "$$").replace(r"\]", "$$")
        clean_text = clean_text.replace(r"\(", "$").replace(r"\)", "$")
        
        # Исправляем экранирование обратных слешей (важно для формул!)
        clean_text = clean_text.replace("\\\\int", "\\int")
        clean_text = clean_text.replace("\\\\frac", "\\frac")
        clean_text = clean_text.replace("\\\\sqrt", "\\sqrt")
        clean_text = clean_text.replace("\\\\ln", "\\ln")
        clean_text = clean_text.replace("\\\\log", "\\log")
        clean_text = clean_text.replace("\\\\sin", "\\sin")
        clean_text = clean_text.replace("\\\\cos", "\\cos")
        clean_text = clean_text.replace("\\\\tan", "\\tan")
        clean_text = clean_text.replace("\\\\lim", "\\lim")
        clean_text = clean_text.replace("\\\\sum", "\\sum")
        clean_text = clean_text.replace("\\\\prod", "\\prod")
        
        # Убираем лишние пробелы вокруг формул
        clean_text = re.sub(r'\$\$\s+', '$$', clean_text)
        clean_text = re.sub(r'\s+\$\$', '$$', clean_text)
        clean_text = re.sub(r'\$\s+', '$', clean_text)
        clean_text = re.sub(r'\s+\$', '$', clean_text)
        
        return clean_text
    except Exception as e:
        return f"Ошибка очистки текста: {str(e)}"

